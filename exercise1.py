"""Ixtiyoriy sonning 3-darajasini hisoblovchi PowerA3 nomli funksiya hosil qiling.
PowerA3 funksiyasi orqali A, B, C haqiqiy sonlarining va D, E butun sonlarining 3-darajasini
hisoblovchi programma tuzilsin."""

def PowerA3(a, b, c, d, e):
    return f"a: {a ** 3}, b: {b ** 3}, c: {c ** 3}, d: {d ** 3}, e: {e ** 3}"
    

print(PowerA3(2, 3, 4, 5.2, -4))